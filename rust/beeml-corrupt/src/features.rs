/// Phoneme substitution cost matrix.
///
/// Returns a cost between 0.0 (identical) and 1.0 (maximally different).
///
/// Every pair of the 39 ARPAbet phonemes has been individually considered.
/// Consonant↔vowel is always 1.0. Within consonants and vowels, costs
/// reflect articulatory similarity AND observed ASR confusion patterns.
///
/// Phoneme inventory:
///   Consonants (24): P B T D K G F V TH DH S Z SH ZH HH CH JH M N NG L R W Y
///   Vowels (15):     IY IH EH EY AE AA AO OW OY UH UW AH ER AW AY
pub fn substitution_cost(a: &str, b: &str) -> f32 {
    if a == b {
        return 0.0;
    }
    let ai = match phoneme_index(a) {
        Some(i) => i,
        None => return 1.0,
    };
    let bi = match phoneme_index(b) {
        Some(i) => i,
        None => return 1.0,
    };
    let (lo, hi) = if ai <= bi { (ai, bi) } else { (bi, ai) };
    COST_MATRIX[lo][hi]
}

